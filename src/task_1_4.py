"""
Даны два действительных числа. Найти среднее арифметическое и
среднее геометрическое этих чисел
"""

"""
the total variable shows the arithmetic average
"""
a = 7.5
b = 8
total = (a + b) / 2
print(total)

"""
the geometric variable shows the geometric average
"""
geometric = pow(a * b, 0.5)
print(geometric)
